function DrawMercatorMap ()
{
	for (a=0; a<Outlines.length; a++)
	{
	/*	if (Outlines[a].length < 3)
		{
			continue;
		} */
		
	//	if (Outlines[a].length >= 4 && Outlines[a].length <= 100)
		{
			if (Outlines[a][1][0] == Outlines[a][Outlines[a].length-1][0] &&
				Outlines[a][1][1] == Outlines[a][Outlines[a].length-1][1])
			{
			//	p = app.activeDocument.polygons.add({numberOfSides:Outlines[a].length-1, strokeWeight:0, fillColor:app.activeDocument.swatches[2]});
				p = app.activeDocument.polygons.add({numberOfSides:3, strokeWeight:0, fillColor:app.activeDocument.swatches[2]});
				pt = new Array;
				for (b=1; b<Outlines[a].length-2; b++)
				{
					pt.push ([Outlines[a][b][0], -Outlines[a][b][1]]);
				}
				p.paths[0].entirePath = pt;
				p.label = Outlines[a][0];
				continue;
			}
		}
		
		l = app.activeDocument.graphicLines.add({strokeWeight:0.1, strokeColor:app.activeDocument.swatches[2]});
		x = Outlines[a][1][0];
		y = -Outlines[a][1][1];
		l.paths[0].pathPoints[0].anchor = [x, y];
	
		x = Outlines[a][2][0];
		y = -Outlines[a][2][1];
		l.paths[0].pathPoints[1].anchor = [x, y];
	
		for (b=3; b<Outlines[a].length; b++)
		{
			x = Outlines[a][b][0];
			y = -Outlines[a][b][1];
			l.paths[0].pathPoints.add({anchor:[x,y]});
		}
		l.label = Outlines[a][0];
		
		if (Outlines[a][1][0] == Outlines[a][Outlines[a].length-1][0] &&
			Outlines[a][1][1] == Outlines[a][Outlines[a].length-1][1])
		{
			l.paths[0].pathType = PathType.CLOSED_PATH;
			l.strokeWeight = 0;
			l.fillColor = app.activeDocument.swatches[2];
			continue;
		}
	
		x = Outlines[a][Outlines[a].length-1][0];
		y = -Outlines[a][Outlines[a].length-1][1];
		l.paths[0].pathPoints.add({anchor:[x,y]});
		
		if (isNearC (Outlines[a][1], Outlines[a][Outlines[a].length-1]))
		{
			l.paths[0].pathType = PathType.CLOSED_PATH;
			l.fillColor = app.activeDocument.swatches[2];
			alert ("outline "+Outlines[a][0]+"!");
			exit(0);
			continue;
		}
	}
}

function isNearC(a,b)
{
  if (isNear (a[0],b[0]))
  {
    if (isNear (a[1],b[1]))
      return true;
  }
  return false;
}

function isNear (a,b)
{
    if (a > b-1 && a < b+1) return true;
    if (b > a-1 && b < a+1) return true;
    return false;
}
